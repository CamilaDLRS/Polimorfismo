public class ManipuladorStrings {

    public void concatenar(String argUm, String argDois) {
        System.out.println(argUm.concat(argDois));
    }

    public void concatenar(String argUm, String argDois, String argTres) {
        System.out.println(argUm.concat(argDois).concat(argTres));
    }

    public void concatenar(String argUm, String argDois, String argTres, String argQuatro) {
        System.out.println(argUm.concat(argDois).concat(argTres).concat(argQuatro));
    }
}
